#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	u;
	char			*p;

	i = 0;
	u = c;
	p = b;
	while (i < len)
	{
		p[i] = u;
		i++;
	}
	return (b);
}
